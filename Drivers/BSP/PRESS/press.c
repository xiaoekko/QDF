/**
 * @brief  重置压力传感器数据
 * @param  None
 * @retval None
 */
void press_reset_data(void)
{
    memset(&g_press_data, 0, sizeof(press_data_t));
    g_press_data.uart_ready = 1;
}

/**
 * @brief  计算CRC16校验值
 * @param  data: 数据指针
 * @param  len: 数据长度
 * @retval CRC16校验值
 */
uint16_t press_crc16_calculate(uint8_t *data, uint8_t len)
{
    uint8_t crc_hi = 0xFF;
    uint8_t crc_lo = 0xFF;
    uint8_t index;
    
    while (len--) {
        index = crc_hi ^ *data++;
        crc_hi = crc_lo ^ crc_hi_table[index];
        crc_lo = crc_lo_table[index];
    }
    
    return ((crc_hi << 8) | crc_lo);
}

/**
 * @brief  IEEE754浮点数转换
 * @param  num_float32: 32位整形数据
 * @retval 转换后的浮点数
 */
float press_float32_convert(uint32_t num_float32)
{
    uint8_t sign_bit = (num_float32 >> 31) & 0x01;
    uint8_t exponent = (num_float32 >> 23) & 0xFF;
    uint32_t mantissa = num_float32 & 0x7FFFFF;
    float result;
    
    // 检查符号位
    if (sign_bit) {
        return -1.0f;
    }
    
    // 检查指数
    if (exponent == 0) {
        return 0.0f;
    }
    
    exponent -= 127;  // 计算实际指数
    
    // 计算结果
    result = (float)((1 << (exponent + 10)) * 1000 / 1024 + 
                     (mantissa >> (13 - exponent)) * 1000 / 1024) / 1000.0f;
    
    // 检查范围
    if (result > PRESSURE_MAX_VALUE) {
        return -1.0f;
    }
    
    return result;
}

/**
 * @brief  压力数据滤波
 * @param  buffer: 数据缓冲区
 * @param  length: 数据长度
 * @retval 滤波后的值
 */
uint32_t press_filter_data(uint32_t *buffer, uint8_t length)
{
    uint8_t i, j;
    uint32_t temp_buf[RS485_GATHER_TIMES];
    uint32_t temp_value;
    uint64_t sum = 0;
    
    // 复制数据
    for (i = 0; i < length; i++) {
        temp_buf[i] = buffer[i];
    }
    
    // 冒泡排序
    for (i = 0; i < length - 1; i++) {
        for (j = i + 1; j < length; j++) {
            if (temp_buf[i] > temp_buf[j]) {
                temp_value = temp_buf[i];
                temp_buf[i] = temp_buf[j];
                temp_buf[j] = temp_value;
            }
        }
    }
    
    // 去掉最大值和最小值
    for (i = RS485_ABORT_TIMES; i < RS485_GATHER_TIMES - RS485_ABORT_TIMES; i++) {
        sum += temp_buf[i];
    }
    
    return (uint32_t)(sum >> 2);  // 右移2位(除以4)
}

/**
 * @brief  处理接收到的压力数据
 * @param  data: 数据指针
 * @param  size: 数据大小
 * @retval None
 */
void press_data_process(uint8_t *data, uint16_t size)
{
    // 检查数据帧
    if (size < MODBUS_FRAME_MIN_SIZE || 
        data[0] != 0x01 || data[1] != 0x03 || data[2] != 0x04) {
        return;
    }
    
    // CRC校验
    g_press_data.temp_crc16 = press_crc16_calculate(data, 7);
    g_press_data.temp_crc_check = (data[7] << 8) | data[8];
    
    if (g_press_data.temp_crc16 != g_press_data.temp_crc_check) {
        return;
    }
    
    // 解析数据
    g_press_data.encode_value = (data[3] << 24) | (data[4] << 16) | 
                                (data[5] << 8) | data[6];
    g_press_data.tmp_adc_value = press_float32_convert(g_press_data.encode_value);
    
    // 累积采样数据
    if (g_press_data.sum_count < RS485_GATHER_TIMES) {
        g_press_data.sum_rx_buf[g_press_data.sum_count] = g_press_data.encode_value;
        g_press_data.sum_count++;
    } else {
        // 进行滤波处理
        g_press_data.tmp_adc_press = press_filter_data(g_press_data.sum_rx_buf, RS485_GATHER_TIMES);
        g_press_data.tmp_press = press_float32_convert(g_press_data.tmp_adc_press);
        g_press_data.up_pressure = g_press_data.tmp_press;
        g_press_data.data_valid = 1;
        
        // 重置采样计数
        g_press_data.sum_count = 0;
    }
}

/**
 * @brief  发送RS485数据
 * @param  buf: 数据缓冲区
 * @param  len: 数据长度
 * @retval None
 */
void press_rs485_send_data(uint8_t *buf, uint8_t len)
{
    if (!g_press_data.uart_ready || buf == NULL || len == 0) {
        return;
    }
    
    g_press_data.uart_ready = 0;
    PRESS_RS485_DE(1);  // 切换到发送模式
    HAL_UART_Transmit_IT(&huart4, buf, len);
}





